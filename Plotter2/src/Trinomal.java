
public class Trinomal {
	double a;
	double b;
	double c;
	double l;
	double r;
	public Trinomal(double a, double b, double c, double l, double r) {
		super();
		this.a = a;
		this.b = b;
		this.c = c;
		this.l = l;
		this.r = r;
	}
	public double getA() {
		return a;
	}
	public double getB() {
		return b;
	}
	public double getC() {
		return c;
	}
	public double getL() {
		return l;
	}
	public double getR() {
		return r;
	}
	
	
}
